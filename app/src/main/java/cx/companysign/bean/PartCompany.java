public class PartCompany {
    private Integer partCompanyId;
    private String  partCompanyName;
    private String   partCompanyAddress;
    private Integer partCompanyCompanyId;
    public Integer getPartCompanyId() {
        return partCompanyId;
    }
    public void setPartCompanyId(Integer partCompanyId) {
        this.partCompanyId = partCompanyId;
    }
    public String getPartCompanyName() {
        return partCompanyName;
    }
    public void setPartCompanyName(String partCompanyName) {
        this.partCompanyName = partCompanyName;
    }
    public String getPartCompanyAddress() {
        return partCompanyAddress;
    }
    public void setPartCompanyAddress(String partCompanyAddress) {
        this.partCompanyAddress = partCompanyAddress;
    }
    public Integer getPartCompanyCompanyId() {
        return partCompanyCompanyId;
    }
    public void setPartCompanyCompanyId(Integer partCompanyCompanyId) {
        this.partCompanyCompanyId = partCompanyCompanyId;
    }
}
